    public static HashMap<String, Jugador> jugadores = new HashMap<>();
    public static Despachador despachador;

    public String jugadorPresente = "";
    public Laberinto lab = new Laberinto(nFilas, nColumnas, altoB, anchoB, laberinto,colorLaberinto);

    public Tablero() { // constructor de la clase tablero
        super();
        setSize(1200, 783);
        setBackground(new Color(22,90,20));
        addKeyListener(this);
    }

    public void paint(Graphics g) {
        for (Jugador j: jugadores.values()) {
            g.setColor(j.login);
            g.fillOval(j.x, j.y,15,15);
            lab.paint(g);
        }
    }

    @Override
    public void keyTyped(KeyEvent keyEvent) {}

    @Override
    public void keyPressed(KeyEvent e) { 

        int [][] laberinto = lab.obtenerLaberinto();
        int x_ = jugadores.get(jugadorPresente).x;
        int y_ = jugadores.get(jugadorPresente).y;

        if(e.getKeyCode()==37){
            if(laberinto[y_/15][(x_/15)-1]!=1) {
                jugadores.get(jugadorPresente).x -= velocidad;
            }
        }
        if(e.getKeyCode()==39){
            if(laberinto[y_/15][(x_/15)+1]!=1) {
                jugadores.get(jugadorPresente).x += velocidad;
            }
        }
        if(e.getKeyCode()==40) {
            if (laberinto[(y_/15)+1][x_/15]!=1) {
                jugadores.get(jugadorPresente).y += velocidad;
            }
        }
        if(e.getKeyCode()==38) {
            if(laberinto[(y_/15)-1][x_/15]!=1) {
                jugadores.get(jugadorPresente).y -= velocidad;
            }
        }

        int _x = jugadores.get(jugadorPresente).x;
        int _y= jugadores.get(jugadorPresente).y;

        despachador.send("mover:"+jugadorPresente+","+_x + ","+_y);
        finalizarJuego();
    }

    public void finalizarJuego(){
        if(jugadores.get(jugadorPresente).x == 690 && jugadores.get(jugadorPresente).y == 720){
            JOptionPane.showMessageDialog(this,"Felicidades" + VentanaPrincipal.jugadorLab);
            VentanaNiveles.finNIvel1();
        }
    }

